package com.example.gtaidp;

public class GyrInfo {

    String X,Y,Z;

    public GyrInfo() {
    }

    public GyrInfo(String x, String y, String z) {
        X = x;
        Y = y;
        Z = z;
    }

    public String getX() {
        return X;
    }

    public void setX(String x) {
        X = x;
    }

    public String getY() {
        return Y;
    }

    public void setY(String y) {
        Y = y;
    }

    public String getZ() {
        return Z;
    }

    public void setZ(String z) {
        Z = z;
    }
}
